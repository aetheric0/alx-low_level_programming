#include "main.h"

/**
 * add - Adds two numbers
 * @a: first operand
 * @b: second operand
 * Return: Result of operation
 **/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtract two numbers
 * @a: first operand
 * @b: second operand
 * Return: Result of operation
 **/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies two numbers
 * @a: first operand
 * @b: second operand
 * Return: Result of operation
 **/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides two numbers
 * @a: first operand
 * @b: second operand
 * Return: Result of operation
 **/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Remainder of division
 * @a: first operand
 * @b: second operand
 * Return: Result of operation
 **/
int mod(int a, int b)
{
	return (a % b);
}
